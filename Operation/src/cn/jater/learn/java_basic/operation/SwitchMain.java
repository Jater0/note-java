package cn.jater.learn.java_basic.operation;

public class SwitchMain {
    public static void main(String[] args) {
        String s = "a";
        switch (s) {
            case "a":
                System.out.println("aaa");
                break;
            case "B":
                System.out.println("BBB");
                break;
        }
    }
}
